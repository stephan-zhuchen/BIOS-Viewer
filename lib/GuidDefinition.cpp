bool EFI_GUID::operator==(EFI_GUID guid) {

    if (this->Data1 != guid.Data1) {
        return false;
    } else if (this->Data2 != guid.Data2) {
        return false;
    } else if (this->Data3 != guid.Data3) {
        return false;
    }
    for (int var = 0; var < 8; ++var) {
        if (this->Data4[var] != guid.Data4[var]) {
            return false;
        }
    }
    return true;
}

